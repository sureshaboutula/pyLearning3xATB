# Dynamically Typed

# Dynamic Type - Type of Data supported by Python

age = 65
# VariableName = VariableValue
# Identifier = Literal

# Data types in Python

# Integers - Positive and negative whole numbers
# 1, 10, -50, 999, 100000, 96543210

# Floating point numbers
# 3.14, 5.33333, 18.00, 0.0076, -0.4, -10.0
pi = 3.14

# String
# "Suresh" or 'Suresh', "A", "Hello World", "I am new to Python", "12345"
name = "Suresh"

# Boolean
# True, False # true and false is wrong

isMale = True

# How do I check the type of the variable?

print(type(age))  # class 'int'
print(type(name))  # class 'str'
print(type(pi))  # class 'float'
print(type(isMale))  # class 'bool'

# Python - Complex Number - i iota
complex_number = 2+3j
print(complex_number.real) # 2
print(complex_number.imag) # 3

