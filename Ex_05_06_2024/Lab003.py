# print(self, *args, sep=' ', end='\n', file=None)

# self - Concept in OOPS
# *args - Unlimited number of  arguments - Integer, Float, String, Boolean....
# sep=' ' - How you want to separate the arguments
# end='\n' - in the end what you want
# file=None - File IO concept

print("Hello", "World", 123, True, 3.14) # Default Separator is space -- same as next line
print("Hello", "World", 123, True, 3.14, sep=" ")
print("Hello", "World", 123, True, 3.14, sep="_")
print("Hi, My name is Suresh", "Aboutula")
print("Hi, My name is Suresh", "Aboutula", sep=" - ")

